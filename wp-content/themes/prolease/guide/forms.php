	<h4>Managing Website Forms</h4>
	
	<p>You can create and manage forms through the Ninja Forms Plugin.</p>

	<h3>Creating a New Form</h3>

	<p>To create a new form, click to 'Ninja Forms' in the admin navigation. Then click the 'Add New' button. Select the 'Blank Form' template.</p>
	
	<dl class="dl-horizontal">
    
    	<dt>Field Types:</dt>
    	<dd>On the 'Form Fields' tab, click the '+' in the lower-right to add fields to the form. Once you have the desired fields, click 'Done' in the top-right. You can then edit the individual fields by clicking on them.</dd>
		
		<dt>Emails & Actions:</dt>
    	<dd>You can add actions to your form that fire after a form is submitted. You can send emails (Email), store the form information (Store Submission), display a message (Success Message), and redirect to another page (Redirect).</dd>

	</dl>

	<h3>Adding a Form to a page</h3>

	<p>To add a form, add a new 'Form Section' to a page. The 'Form Section' requires a 'Ninja Form ID'. Each form has it's own unique number and can be found when you view the all the forms through the Ninja Forms tab in the admin navigation.</p>

