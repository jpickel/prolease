	<h4>Managing Users</h4>
	<h3>Granting user access to edit content on your website</h3>

    <p>You can add and remove users by clicking on the 'Users' link in the admin menu. In the CMS, there are two permission levels (roles) you will use most often:</p>
    
    <dl class="dl-horizontal">
    
        <dt>Editors:</dt>
        <dd>Editors can manage website content only. They do not have the ability to manage website settings, menus, and add or remove users.</dd>
        
        <dt>Administrator:</dt>
        <dd>Administrators have the ability to manage all aspects of the website, including content, settings, menus, and users.</dd>
        
    </dl>
    
    <h3>Adding a New User</h3>
    
    <p>To add a user, click 'Add New' and give the user a username, email address, and password. You can click the checkbox to send that user their information to the email address specified.</p>
    
    <h3>Editing a User</h3>
    
    <p>To edit a user, click 'All Users' and then click on the user you wish to edit. You can change permissions, or remove the user entirely.</p>
    
