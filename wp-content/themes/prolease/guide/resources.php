    <h4>Resources</h4>

    <p>You can easily manage the downloadable resources offered on the website.</p>
    
    <h3>Adding a New Resource</h3>
    
    <p>To add a new resource, click to 'Resources' in the admin navigation. Then click the 'Add New' button. Then add the following details:</p>
    
    <dl class="dl-horizontal">
    
    	<dt>Title:</dt>
    	<dd>This will be the title of the resource seen on the website.</dd>
    	
    	<dt>Resource Type:</dt>
    	<dd>This will identify the appropriate icon to be used alongside the download.</dd>
    	
    	<dt>Resource File:</dt>
    	<dd>This should be a PDF document with a maximum size of 128Mb.</dd>
    	
    </dl>

</div>