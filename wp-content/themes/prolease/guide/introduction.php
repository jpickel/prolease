    <h4>Introduction</h4>
    
    <h3>What is a CMS?</h3>
    
    <p>CMS stands for Content Management System.</p>
    
    <p>A CMS is basically online software that let's companies update their website's content, without needing to know or understand code.</p>
    
    <h3>Toggling Between Website & CMS</h3>
    
    <p>After you log into the CMS, you'll be able to easily toggle between the website and the CMS.</p>
    
    <dl class="dl-horizontal">
    	<dt>Go to the Website</dt>
    	<dd>Click on the 'Go to Website' button at the top of the screen.</dd>
    	<dt>Go to Dashboard</dt>
    	<dd>Hover to the bottom of the screen. An admin menu will appear. Click on the 'Go to Dashboard' button.</dd>
    </dl>
    
