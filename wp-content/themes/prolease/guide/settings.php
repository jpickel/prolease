    <h4>Website Settings</h4>
    
    <h3>Site Settings</h3>
    <p>You may need to modify the website's settings from time to time. All settings options are located in the admin under 'Settings'. Below are the sections you should familiarize yourself with the most.</p>
    
    <dl class="dl-horizontal">
    	
    	<dt>General:</dt>
    	<dd>These settings include the website URL, registration, and time preferences.</dd>
    	
    	<dt>Reading:</dt>
    	<dd>These settings include the standard number of posts to display on a single page and permissions for search engine robots.</dd>
    	
    	<dt>Permalinks:</dt>
    	<dd>These settings are for how "pretty links" are displayed.</dd>
    	
    </dl>

    <h3>Theme Customization</h3>
    <p>All content related to the theme or areas that are part of the template and not specific to an individual page reside in the admin menu under 'Customize Site'. Below are the theme options you should familiarize yourself with the most.</p>
    
    <dl class="dl-horizontal">
    	
    	<dt>Site Identity:</dt>
    	<dd>These options include the company name, tagline, logo and icons, and contact information.</dd>
    	
    	<dt>Theme Images:</dt>
    	<dd>These options include various images that are used as defaults on the website.</dd>
    	
    </dl>

