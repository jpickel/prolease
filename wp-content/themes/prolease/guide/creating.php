	<h4>Creating & Deleting Content</h4>
	
	<h3>Creating New Pages or Blog Posts</h3>

    <p>To create a new page or blog post, first click to the appropriate section in the admin menu. Then, click on the 'Add' button at the top of the page (you can also click on the 'Add' link listed in the expanded admin menu.</p>
    
    <p>Give your page a name, and provide the following information:</p>
    
    <dl class="dl-horizontal">
    
        <dt>Status:</dt>
        <dd>The default statuses are 'Draft' and 'Published'. You can use these to determine if content is ready to be live on the website.</dd>
        
        <dt>Visibility:</dt>
        <dd>You can optionally select a page or post to be publicly available or behind a password (that you provide).</dd>
        
        <dt>Published On:</dt>
        <dd>You can optionally schedule a page or post to publish on a future date.</dd>
    
    	<dt>Categories:</dt>
    	<dd>These are groups of topics your blog is about. The CMS can use this information to display content based on a grouping.</dd>
    	
    	<dt>Tags:</dt>
    	<dd>These are additional category-type tags, but are used more like #hashtags. Hashtags could be used on posts or pages from all different category types.</dd>
    	
    </dl>
    
    <p>Not all of these fields are always available for all content types. Additionally, extra fields may be included in the CMS, according to the type of content it is.</p>
    
    <h3>SEO Options</h3>
    
    <p>The CMS will pull content from your page or post to complete search engine tags. However, you can manually control these tags:</p>
    
    <dl class="dl-horizontal">
    
    	<dt>SEO Title Tag:</dt>
    	<dd>This information appears as the blue link in a Google search result. It should contain a maximum of 70 characters and provide a clear description of the post or page.</dd>
    	
    	<dt>SEO Description:</dt>
    	<dd>This information appears as an excerpt, below the blue link in a Google search result. It should contain a maximum of 160 characters and provide an accurate summary of the page's intent.</dd>
    	
    	<dt>SEO Keywords:</dt>
    	<dd>These keywords are used to tell search engines what topics a page is about. When users search Google for a particular topic, Google evaluates these keywords against your website and the user's search request to match the two up. Keywords work best when they align with the page's content and should be separated by a comma.</dd>
    	
    </dl>
    
    <h3>Deleting Content</h3>
    
    <p>If you need to remove a page or post, first navigate to the main page for that content type. You can hover over a piece of content and select the 'Delete' link that appears below it, or click on the 'Move to Trash' when you are actively editing a page.</p>
    
