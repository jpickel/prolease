	<h4>Managing Website Navigation</h4>
	<p>Menus that appear on the website can be controlled through the 'Navigation' link in the admin menu.</p>
	
	<h3>Switching Between Menus</h3>
	<p>To select a menu to edit, choose it from the dropdown menu and click 'select'.</p>
	
	<h3>Adding Menu Items</h3>
	<p>To add a menu item:</p>
	<ol>
	    <li>Use the sidebar to find pages, blog posts, resources, or even add a custom link.</li>
	    <li>Select the checkbox next to a page and click the <span class="btn btn-default">Add to Menu</span> button.</li>
	    <li>That menu item will appear at the bottom of the menu on the right. Click and drag it up to reorder the list.</li>
	    <li><strong>Note:</strong> If you indent as your drag, this will create a child link and will not appear as a top-level link.</li>
	    <li>Click the <span class="btn btn-primary">Save Menu</span> button to save your changes.</li>
	</ol>
	
	<h3>Editing Menu Items</h3>
	<p>To edit a menu item:</p>
	<ol>
	    <li>Click the arrow next to a menu item to expand it.</li>
	    <li>Modify the label, title, or apply custom classes.</li>
	    <li>Click the <span class="btn btn-primary">Save Menu</span> button to save your changes.</li>
	</ol>
	
	<h3>Removing Menu Items</h3>
	<p>To remove a menu item:</p>
	<ol>
	    <li>Click the arrow next to a menu item to expand it.</li>
	    <li>Click on the 'Remove' link.</li>
	    <li>Click the <span class="btn btn-primary">Save Menu</span> button to save your changes.</li>
	</ol>
	
</div>