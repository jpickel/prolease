    <h3>Adding Links</h3>
    <p>To link text, first <span class="highlight">Highlight</span> the text you wish to link. Then, click on the Link <span class="btn btn-default"><span class="glyphicon glyphicon-link" aria-hidden="true"></span></span> button.</p>
    <p>Type in a URL or a page name to search your website's pages. If you want to add custom settings (like opening up the link in a new tab) click the settings <span class="btn btn-default"><span class="glyphicon glyphicon-cog" aria-hidden="true"></span></span> button.</p>
    <p>When you are finished with your link, click the enter button.</p>
    
